# Integer
my_int = 10
print("Integer:", my_int)

# Float
my_float = 10.5
print("Float:", my_float)

# String
my_string = "Hello, World!"
print("String:", my_string)

# Boolean
my_bool = True
print("Boolean:", my_bool)

# List
my_list = [1, 2, 3, 4, 5]
print("List:", my_list)

# Tuple
my_tuple = (1, 2, 3, 4, 5)
print("Tuple:", my_tuple)

# Dictionary
my_dict = {"name": "John", "age": 30}
print("Dictionary:", my_dict)

# Set
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set)

# NoneType
my_none = None
print("NoneType:", my_none)